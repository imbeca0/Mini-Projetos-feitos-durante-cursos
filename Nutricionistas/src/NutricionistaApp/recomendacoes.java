    public void calcular() {
        double caloriasDiarias;
        double carboidratosMensagem;
        double proteinasMensagem;
        double gordurasMensagem;

        caloriasDiarias = Double.parseDouble(calorias.getText());
        carboidratosMensagem = caloriasDiarias * 0.5 / 4;
        proteinasMensagem = caloriasDiarias * 0.25 / 4;
        gordurasMensagem = caloriasDiarias * 0.25 / 9;

        carboidratos.setText("" + carboidratosMensagem);
        proteinas.setText("" + proteinasMensagem);
        gorduras.setText("" + gordurasMensagem);
    }

