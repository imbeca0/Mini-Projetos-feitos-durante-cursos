        if (strAltura.isEmpty() || strPeso.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Todos os campos precisam ser preenchidos.");
        } else {
            try {
                // Tenta converter as strings para números
                double alturaValor = Double.parseDouble(strAltura);
                double pesoValor = Double.parseDouble(strPeso);

                // Verifica se os valores são válidos
                if (alturaValor <= 0 || pesoValor <= 0) {
                    JOptionPane.showMessageDialog(null, "Os valores precisam ser maiores que 0.");
                } else {
                    // Se todos os critérios forem atendidos, chama o método calc()
                    calc();
                }
            } catch (NumberFormatException e) {
                // Captura a exceção se a conversão falhar (entrada não numérica)
                JOptionPane.showMessageDialog(null, "Entrada inválida. Digite valores numéricos válidos.");
            }
        }
