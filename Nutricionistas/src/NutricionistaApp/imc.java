     public void calc() {
        //variaveis
        double altura;
        double peso;
        double imc;
        String mensagem;
        String imcResposta;
        String nomeApresentar;
        //capturando o que o usuario digitou
        altura = Double.parseDouble(ResAltura.getText());
        peso = Double.parseDouble(ResPeso.getText());

        //calculo
        imc = peso / (altura / 100 * altura / 100);

        //imc
        if (imc < 18.5) {
            imcResposta = "" + imc;
            mensagem = "\nMagreza, se o IMC for menor que 18,5";
        } else if (imc < 24.9) {
            imcResposta = "" + imc;
            mensagem = "\nNormal, se o IMC for entre 18,5 e 24,9";
        } else if (imc < 29.9) {
            imcResposta = "" + imc;
            mensagem = "\nSobrepeso, se o IMC for entre 25 e 29,9";
        } else if (imc < 30) {
            imcResposta = "" + imc;
            mensagem = "\nObesidade, se o IMC for entre 30 e 39,9";
        } else {
            imcResposta = "" + imc;
            mensagem = "\nObesidade grave, se o IMC for maior ou igual a 40";
        }
        ResultadoImc.setText(imcResposta);
        InterpretacaoImc.setText(mensagem);
    }

