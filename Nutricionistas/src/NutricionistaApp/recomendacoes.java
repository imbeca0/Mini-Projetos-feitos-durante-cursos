    private void calcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcularActionPerformed

        String strCalorias = calorias.getText();

        if (strCalorias.isEmpty()) {
            JOptionPane.showMessageDialog(null, "O campo Calorias deve ser preenchido.");
        } else {
            try {
                // Tenta converter a string para um número decimal (double)
                double caloriasValor = Double.parseDouble(strCalorias);

                // Verifica se o valor é maior que zero
                if (caloriasValor <= 0) {
                    JOptionPane.showMessageDialog(null, "Os valores precisam ser maiores que 0.");
                } else {
                    // Se o valor for válido, chama o método calcular()
                    calcular();
                }
            } catch (NumberFormatException e) {
                // Captura a exceção se a conversão falhar (entrada não numérica)
                JOptionPane.showMessageDialog(null, "Entrada inválida, a entrada deve ser em números.");
            }
        }
    }//GEN-LAST:event_calcularActionPerformed

